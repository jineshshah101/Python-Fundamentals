# Integer Data Type
# numbers that aren't decimals

four = 2 + 2
print(four)

# Float Data Type
# Decimal numbers

fivepointfive = 5.5
print(fivepointfive)

# String Data Type
# Things in quotations are Strings

Pride = "The Lion Sin of Pride, Sir Escanor"
print(Pride)

# Boolen Data Type
# Either True or False ie Yes or No

print(True)
print(False)