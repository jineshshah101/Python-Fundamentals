import turtle

# made one turtle
squirtle_turtle = turtle.Turtle()

# creating a function to make a square
def squirtle():
    squirtle_turtle.forward(200)
    squirtle_turtle.right(90)
    squirtle_turtle.forward(200)
    squirtle_turtle.right(90)
    squirtle_turtle.forward(200)
    squirtle_turtle.right(90)
    squirtle_turtle.forward(200)
    squirtle_turtle.showturtle()

# call on the function to make the square
squirtle()

# making a pair of glasses
squirtle_turtle.right(90)
squirtle_turtle.forward(400)
squirtle()